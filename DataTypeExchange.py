# int() 强制转换为整型：
x = int(1)   # x 输出结果为 1
y = int(2.8) # y 输出结果为 2
z = int("3") # z 输出结果为 3

# float() 强制转换为浮点型：
a = float(1)     # x 输出结果为 1.0
b = float(2.8)   # y 输出结果为 2.8
c = float("3")   # z 输出结果为 3.0
d = float("4.2") # w 输出结果为 4.2

# str() 强制转换为字符串类型：
a1 = str("s1") # x 输出结果为 's1'
a2 = str(2)    # y 输出结果为 '2'
a3 = str(3.0)  # z 输出结果为 '3.0'

